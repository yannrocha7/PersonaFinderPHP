            // Connect to the database
            $servername = "localhost";
            $username = "root";
            $password = "";
            $database = "PersonaFinder";

            $conn = new mysqli($servername, $username, $password, $database);

            // Check if the database connection was successful
            if ($conn->connect_error) {
                die("Error connecting to the database: " . $conn->connect_error);
            }

            // Prepare and execute the query to retrieve the personal's name based on their email
            $sqldadospersonal = "SELECT * FROM personal WHERE cpf = '$cpf'";
            $resultdadospersonal = $conn->query($sqldadospersonal);

            if ($resultdadospersonal && $resultdadospersonal->num_rows > 0) {
                $row = $resultdadospersonal->fetch_assoc();
